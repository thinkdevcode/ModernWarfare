#ifndef _GTPVector2_
#define _GTPVector2_

#include <math.h>

/////////////////////////////////////////////////
/// The gtp namespace is a suite of methods and data 
/// used by gtproductions.org
/////////////////////////////////////////////////
namespace gtp
{
	namespace math
	{
		/////////////////////////////////////////////////
		/// Vector2 (Float)
		/// Holds an x and a y and supports overloading
		/////////////////////////////////////////////////
		struct Vector2
		{
			
			/////////////////////////////////////////////////
			/// Default Constructor
			///		Takes no paramaters. Sets default values for x&y
			Vector2();
			/////////////////////////////////////////////////

			/////////////////////////////////////////////////
			/// Another Constructor
			///		Takes the x and y values
			Vector2(float x, float y);
			/////////////////////////////////////////////////

			/////////////////////////////////////////////////
			/// = Operator overloader
			///		Allows Vector2's to be equaled to each other
			Vector2& operator=(const Vector2& param);
			/////////////////////////////////////////////////

			/////////////////////////////////////////////////
			/// Takes 2 coords (Target and Current)
			///		Finds the delta of the two
			void DeltaVector(float TargetX, float TargetY, float CurrentX, float CurrentY);
			/////////////////////////////////////////////////

			float x;	///< default x = 0
			float y;	///< default y = 0
		};

		/////////////////////////////////////////////////
		/// Normalize()
		/// Finds the delta between 2 coords (aka Slope)
		/// and then normalizes them by reducing to 1 unit of
		/// length.
		Vector2 Normalize(Vector2 Target, Vector2 Current);
		/////////////////////////////////////////////////

		/////////////////////////////////////////////////
		/// Distance()
		/// Finds the distance between two vectors
		float Distance(Vector2 Target, Vector2 Current);
		/////////////////////////////////////////////////
	}
}

#endif